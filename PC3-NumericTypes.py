# There are three numeric types in Python:
    # • Int
    # • float
    # • complex

x = 1 # int
y = 2.8 # float
z = 1j # complex

print(type(x))
print(type(y))
print(type(z))

# Int

print("\nIntegers")

x = 1
y = 785487874887711
z = -89722

print(type(x))
print(type(y))
print(type(z))

# Float

print("\nFloat")

x = 1.10
y = 1.0
z = -3785.59

print(type(x))
print(type(y))
print(type(z))

# Float - E indicates to th power 10
x = 35e3
y = 12E4
z = -87.7e100

print(type(x))
print(type(y))
print(type(z))

# Complex

print("\nComplex")

x = 78+15j
y = 45j
z = -89j

print(type(x))
print(type(y))
print(type(z))

